public class Personaje {
    private String nombre;
    private int fuerza;
    private int edad;

    public Personaje(String nombreIn, int fuerzaIn, int edadIn) {
        this.nombre = nombreIn;
        this.fuerza = fuerzaIn;
        this.edad = edadIn;
    }

    public String getNombre() {
        return nombre;
    }

    public int getFuerza() {
        return fuerza;
    }

    public int getEdad() {
        return edad;
    }
    
    
    
    
    
}
